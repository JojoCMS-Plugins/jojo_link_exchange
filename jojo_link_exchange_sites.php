/*
Logic:
Reciprocal linking is not as good as one way linking.
A mixture of link text looks more natural than the same phrase over and over.
Totally random link text is bad, link text changing all the time is not natural.
Links pages should look somewhat unique, to avoid duplicate content issues.
Nobody really wants to link to mesothelioma or premature ejaculation sites.

CMS sites are grouped into 5 groups, so one way links are always used.
Each site in group A will link to all of group B and group C.
Each site in group B will link to all of group C and group D.
Each site in group C will link to all of group D and group A.
Each site in group D will link to all of group A and group B.
Sites in group X link to other sites in group X (recip) only.

This means each site links to a number of other sites, without reciprocating. Some
thought has gone into balancing the sites so each group has a mixture of established
good ranking sites, and new sites with no ranking. This should ensure that sites in each
group help each other roughly equally. Some thought has also gone into ensuring sites
don't link to their competetion.

